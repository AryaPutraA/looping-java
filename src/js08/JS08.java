public class JS08 {
    public static void main(String[] args) {
         
        //For
        int a;
        for(a = 1;a <=5; a++){
            System.out.println("SMK Telkom Malang");}
        
        //While
        int b = 1;
        while(b <= 5){
            System.out.println("SMK Telkom Malang");
            b++;}
        
        //DO While
        int c = 1;
        do{
            System.out.println("SMK Telkom Malang");
            c++;}
        while(c <= 5);
    

    }
    
}
